bool Application::initialize() {
	return true;
}
float r = 0.;
void Application::update() {
	static bool triggered = false;
	if (!triggered) {
		upload();
		triggered = true;
	}
	else {
		r += 1.;
		boxConstantBufferPtr->world = Game::PackTransfrom(Game::Vector3(0., 0., 9), Game::Vector3(0., r * .1, 0.), Game::Vector3(1., 1., 1.));
		boxConstantBufferPtr->transInvWorld = boxConstantBufferPtr->world.R();
		boxConstantBufferPtr->world = boxConstantBufferPtr->world.T();

		Camera* mainCamera = gGraphic.GetMainCamera();
		mainCamera->setRotaion(Game::Vector3(sinf(r * 1e-3) * 15., 0., 0.));
		

		gGraphic.BindShader(shader);
		gGraphic.BindMainCameraPass();
		gGraphic.BindConstantBuffer(boxConstantBuffer.Get(), 0);
		gGraphic.Draw(box->GetVBV(), 0, box->GetVertexNum());
	}
}

void Application::finalize() {
	box.release();
}